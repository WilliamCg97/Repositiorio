# Utilizando operador soma " + "   
numero1 = 2
numero2 = 4 

soma = numero1 + numero2 
print("A soma de" , numero1 , "+" , numero2 , "=" , soma ) 
 
  #Operador de subtração "-"
 
subtracao = numero2 - numero1
 
print("A subtração de ", numero2 , "-" , numero1 , "=", subtracao )

#Operador multiplicação "."
numero_usuario_1 = int(input("Digite um valor entre 0 a 10 : "))
numero_usuario_2 = int(input("Digite um valor entre 0 a 10 : "))
multiplicacao = numero_usuario_1 * numero_usuario_2 
print ("O produto de " , numero_usuario_1 , "*" , numero_usuario_2 , "=" , multiplicacao)


# Operador Divisão " / "

divisao = numero_usuario_1 / numero_usuario_2 
print(f" A divisão {numero_usuario_1 } / {numero_usuario_2} é igual a {divisao} ") 




