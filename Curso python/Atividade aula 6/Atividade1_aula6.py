## Faça um programa que mostre na tela uma contagem
## regressiva para o estouro de fogos de artificio, indo de 10 até 0,
## com uma pausa de 1 segundo entre eles.
import time

##                  i   f   s 

for fogo in range (10,0,-1):
    print(fogo)
    time.sleep(1)
else : 
    print("Fogo :d  :D ")